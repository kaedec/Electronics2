%--------------------------------------------------------------------
function [Z] = Polar_2_Rect(R, theta)
%--------------------------------------------------------------------
% Converts R theta to x + j*y
% theta is assumed to be in degrees
%--------------------------------------------------------------------

% XF = 180/pi;
% 
% Z = R .* exp(j*theta/XF);
Z = R .* (cosd(theta) + 1j * sind(theta));

