%--------------------------------------------------------------------
function Print_Z(S, Z)
%--------------------------------------------------------------------
%
% Usage: Print_Z(S, Z)
%
% Purpose: Print an Impedance 
%
% Parameters: 
%            S - String
%            Z - Impedance
%
%--------------------------------------------------------------------

R = real(Z);
X = imag(Z);

%--------------------------------------------------------------------

if X < 0
    fprintf('\n %s = (%4.4f - j%4.4f) Ohms\n',S, R, abs(X));
else
    fprintf('\n %s = (%4.4f + j%4.4f) Ohms\n',S, R, abs(X));
end

%--------------------------------------------------------------------
